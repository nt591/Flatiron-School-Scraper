CREATE TABLE students (
  id INTEGER PRIMARY KEY,
  name VARCHAR(255),
  tagline VARCHAR(255),
  image_url VARCHAR(255),
  bio TEXT,
  email VARCHAR(255),
  blog VARCHAR(255),
  linkedin VARCHAR(255),
  twitter VARCHAR(255),
  fav_apps_one TEXT,
  fav_apps_two TEXT,
  fav_apps_three TEXT,
  codeschool VARCHAR(255),
  github VARCHAR(255),
  coderwall VARCHAR(255),
  stack VARCHAR(255),
  treehouse VARCHAR(255)
);